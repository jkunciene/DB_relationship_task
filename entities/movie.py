class Movie:

    def __init__(self, moviesId, movie_name, release_date, rating, genre, studioId, boxofficeId):
        self.moviesId = moviesId
        self.movie_name = movie_name
        self.release_date = release_date
        self.rating = rating
        self.genre = genre
        self.studioId = studioId
        self.boxofficeId = boxofficeId
